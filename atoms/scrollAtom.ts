import { atom, useRecoilState } from 'recoil';

export const scrollYState = atom<number>({
    key: 'scrollYState',
    default: 0,
});

export const useScrollYStore = () => {
    const [scrollY, setScrollY] = useRecoilState(scrollYState);

    const setScrollYValue = (newScrollY: number) => {
        setScrollY(newScrollY);
    };

    return {
        scrollY,
        setScrollY: setScrollYValue,
    };
};
